WITH DRIVER_TEAM AS (
    SELECT
        CASE WHEN a12.DRIVER_NUMBER IS NULL THEN '0' ELSE a12.DRIVER_NUMBER END AS DRIVER_NUMBER,
        a12.DRIVER_CODE,
        a11.DRIVER_ID,
        a11.TEAM_ID,
        CONCAT('#', a13.CONSTRUCTOR_COLOUR) AS CONSTRUCTOR_COLOUR
    FROM SESSION.CLEANED_PRACTICE_DATA a11
    LEFT JOIN REFERENCE.DIM_DRIVER a12
        ON a11.DRIVER_ID = a12.DRIVER_ID
    LEFT JOIN REFERENCE.DIM_CONSTRUCTOR a13
        ON a11.TEAM_ID = a13.CONSTRUCTOR_ID
    WHERE 
        EVENT_CD = {year}{round_num}
),

QUALI_DATA AS (
    SELECT
        EVENT_CD,
        DRIVER_ID,
        Q_POSITION,
        Q_TIME
    FROM SESSION.QUALIFYING_RESULTS
    WHERE
        SESSION_CD = 4
        AND EVENT_CD = {year}{round_num}
)

SELECT
    CONCAT(DRI.DRIVER_CODE, '-', DRI.DRIVER_NUMBER) AS DRIVER,
    DRI.CONSTRUCTOR_COLOUR,
    CAST(@s:=@s+1 AS UNSIGNED) AS PREDICTED_POSITION,
    ACT.Q_POSITION AS ACTUAL_POSITION,
    PRED.PREDICTED_LAPTIME_Q,
    ROUND(ACT.Q_TIME - PRED.PREDICTED_LAPTIME_Q, 3) AS LAPTIME_DIFFRENCE,
    ABS(ROUND(ACT.Q_TIME - PRED.PREDICTED_LAPTIME_Q, 3)) AS ABS_LAPTIME_DIFFRENCE
FROM PREDICTION.QUALIFYING_PREDICTION_DATA PRED
LEFT JOIN QUALI_DATA ACT
    ON PRED.EVENT_CD = ACT.EVENT_CD
    AND PRED.DRIVER_ID = ACT.DRIVER_ID
LEFT JOIN DRIVER_TEAM DRI
    ON PRED.DRIVER_ID = DRI.DRIVER_ID, (select @s:=0) as s
WHERE 
    PRED.EVENT_CD = {year}{round_num};